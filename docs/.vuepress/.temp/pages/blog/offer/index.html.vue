<h2 id="_02-你经历过找实习的痛苦吗" tabindex="-1"><a class="header-anchor" href="#_02-你经历过找实习的痛苦吗" aria-hidden="true">#</a> 02 你经历过找实习的痛苦吗</h2>
<p>到了大三暑假，原本没有紧张的氛围一下子变得无比紧张，因为有同学找到大厂实习了。</p>
<p>对于别人找到实习的消息，我是一边的羡慕又嫉妒呀，想着自己本来是可以找到实习的。没错自己的入门是比很多人晚，我是<code v-pre>大三的时候</code>才开始真正学习一门技术，才开始真正的接触前端这个职业。虽然说很多人都没有方向，甚至不知道计算机的出路在哪的时候，我就开始了自己的想法，起码比他们有了一点经验，因为<code v-pre>我确定了自己的方向</code>。</p>
