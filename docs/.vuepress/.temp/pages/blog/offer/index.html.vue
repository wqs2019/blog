<h3 id="人生第一份工作" tabindex="-1"><a class="header-anchor" href="#人生第一份工作" aria-hidden="true">#</a> 人生第一份工作</h3>
<p>所以这是真正意义上的第一份工作，第一份从事技术类型的工作，紧张又期待。</p>
<p>虽然自己也知道<code v-pre>社会工作与学校生活不同</code>，但是有很多事情你是必须要考虑的。比如<code v-pre>租房</code>、<code v-pre>上班</code>和其他事情。很多时候，就像在学校的时候自己是完全不用考虑住房的问题的，但是出去外面的时候你就不得不面对这个问题，开始接受社会的毒打了。</p>
<ul>
<li>租房</li>
<li>独立生活</li>
<li>准时上下班</li>
<li>没有课间休息</li>
<li>中午不能躺床上</li>
<li>饭堂没有学校便宜</li>
<li>工资刚好能维持生活</li>
</ul>
<p>上面的这些问题都是你必须考虑到的，除此之外还是有很多事情不能如愿的，当你经历过实习的时候你就会更加向往学校的咸鱼生活。不过想想，实习就是这样的，舒服是留给死人的。正如那句话说的，<code v-pre>死于25，葬于75</code>。</p>
<h3 id="第一次感受工作" tabindex="-1"><a class="header-anchor" href="#第一次感受工作" aria-hidden="true">#</a> 第一次感受工作</h3>
<p>自己在学校学的东西基本上是没有用到工作当中去，第一个原因是这个公司的技术栈是比较老的，还是用着2015年前的技术，前期基本上是看公司文档学习，看源代码等慢慢熟悉公司的业务。</p>
<p>那时候自己就想着，以后自己的人生就是这样了吗？想想还是有一些不愿意的，毕竟跟自己想象中的不一样，我开始有一点害怕了，<code v-pre>开始变得急躁与焦虑</code>，感觉生活开始对我下手了，毫无还手之力的我只好默默承受着这一切。</p>
<ul>
<li>上班一天比上课一天劳累</li>
<li>周末比上班还累</li>
<li>没有娱乐生活</li>
<li>没有朋友交谈</li>
</ul>
<p>这些都是我在实习中遇到的困难、上班当中的真实感受。所以经历了3个月左右的实习，自己选择了离开。那时候其实还挺矛盾的，公司培养你这么久你却要离开，但是仔细想想还是有自己想法，觉得自己有更大的机会，<code v-pre>感谢当初自己的选择</code>。</p>
<h3 id="第一次工作感悟" tabindex="-1"><a class="header-anchor" href="#第一次工作感悟" aria-hidden="true">#</a> 第一次工作感悟</h3>
<p>工作都是为了公司服务，<code v-pre>能做东西才是最好的</code>。</p>
<p>在一开始的那段时间，显得尤为难熬。虽然看起来你整天都是无所事事，但是你却不知道要干嘛，没人会在你身边教你，因为别人比你还忙，自己只能靠着仅有的一点智商猜测这个功能到底是干嘛用的。这是真实的情况，那时候我和我一朋友就是在这种懵逼的状态下度过了一段的时间。佩服佩服。</p>
<ul>
<li>理论无用，动手才是王道</li>
<li>加班是必须的操作</li>
<li>真正能帮你的只有自己</li>
<li>要有自己思考，不要让别人喂你</li>
<li>要给自己信心坚持下去</li>
</ul>
<p>上面的一些是我在实习的时候总结出来的，<code v-pre>实习就是等于提前接触这个社会</code>，接触将来自己要面对的生活环境、工作环境等。我认为最后一点才是重要的，要给自己信心走下去，要<code v-pre>不断暗示自己可以</code>的，一个人要是内心垮掉了基本上就没有动力了。</p>
