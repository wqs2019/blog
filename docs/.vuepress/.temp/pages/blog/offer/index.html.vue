<h3 id="开始写简历了" tabindex="-1"><a class="header-anchor" href="#开始写简历了" aria-hidden="true">#</a> 开始写简历了</h3>
<p>之前的那些经历不是让我最慌的，最慌的是写简历。
众所周知，计算机的专业是没有维修电脑的。但是我后来才知道的是，计算机专业学的东西原来在<code v-pre>企业中是不适用</code>的。怎么说呢？因为在写简历的阶段，我也找了很多种怎么写简历的方法、简历内容编写等等，然后我发现，自己的大学到底学了啥？为什么自己学的东西都跟我将来工作的差别这么大？小朋友，你是否有很多问号</p>
<p>没事，<code v-pre>大学生的学习效率是很高的</code>，举例来说你即使一个学期没有听课，前一晚上的努力学习你也不会挂科。所以我们就拿出大学学习的效率，认真准备了简历上面的内容，你要考啥我就复习啥，问你怕不怕？</p>
<h3 id="开始投简历了" tabindex="-1"><a class="header-anchor" href="#开始投简历了" aria-hidden="true">#</a> 开始投简历了</h3>
<p>如无意外，我们一定是准备好了再投递简历的，但是如无意外你是没有面试机会的。怎么说呢？大部分的同学都是在<code v-pre>准备面试的时候就直接投递简历了</code>，但那时自己却没有这种想法也没有这种胆量。想想自己是几斤几两还是知道的，况且自己没有面试过任何一家公司，想想那种空前的压力有多大。
经过同学给的建议是先<code v-pre>尝试一下那些小的公司试一下水</code>，不要一开始就找你想要进的公司。所以我就照做了，那时候自己的无知、年少和满腔热血，把小公司都去海投了一遍。令我非常惊讶的是：<code v-pre>竟然没有一家有面试机会</code>？</p>
<h3 id="开始自闭阶段了" tabindex="-1"><a class="header-anchor" href="#开始自闭阶段了" aria-hidden="true">#</a> 开始自闭阶段了</h3>
<p>想着自己的几个月的付出现在却没有回音，想想难免会觉得心酸，难过，伤心...</p>
<p>不过虽然自己有那么一段的时间是属于<code v-pre>自闭的状态</code>，不想和别人说关于工作的事情。但是自己每天都有一种紧张的心态，因为万一有一家打电话过来了呢？不过没有万一。</p>
<p>我从一开始就没有注意到自己的问题，遇事都会觉得是其他的原因。虽然我知道是我的原因，我就是不愿意承认。对于自己第一次认清自己狼狈的面貌是有点不愿意，毕竟没有喜欢认可自己的不足，也不喜欢承认自己的原因、缺点。经过多方的思想斗争，跟同学朋友聊了之后我就开始<code v-pre>慢慢接受自己的不足</code>了，决心从简历上面下手修改。</p>
<h3 id="开始自我怀疑了" tabindex="-1"><a class="header-anchor" href="#开始自我怀疑了" aria-hidden="true">#</a> 开始自我怀疑了</h3>
<p>果不其然，修改了简历之后，自己有了第一次的面试机会。那时候是师兄进行的内推的，投了简历之后想着找两天时间好好从头过一遍简历上面的内容，但是没想到不多3个小时就来了电话。我裂开了</p>
<p><img src="https://imgkr.cn-bj.ufileos.com/8445e891-1ba1-4761-9515-60998dbfc31a.png" alt=""></p>
<p>这个面试题我至今还保留，是自己的第一次面试。想想那时候的自己是多么紧张，我接到电话的那一瞬间整个人都僵住了，甚至连自我介绍都没有准备，直接开面。</p>
<p>顺便说一下，上面的面试题基本上没有答出来，甚至自己连cookie与session的区别都没有搞清楚，我真的是优秀。如无意外，稳挂的面试没什么可说的。</p>
<p>但是自己变得<code v-pre>更加怀疑</code>了，难道网上说的前端面试题都是骗人的吗？怎么都没有出现哪些常规的面试题，这不是为难我胖虎吗？一度<code v-pre>怀疑自己不适合搞技术</code>，不适合面试。自己跟很多的小伙伴一样，过度关注前端的题库，忘记了计算机基础的内容，其实上面的计算机组成原理在大学都学过，但是谁又会认真记下来呢？</p>
<h2 id="_03-开始小公司实习" tabindex="-1"><a class="header-anchor" href="#_03-开始小公司实习" aria-hidden="true">#</a> 03 开始小公司实习</h2>
<p>说来惭愧，自己那时候除了面试上面的这一家公司之外自己就没有其他机会了。
但是幸运的是，同学介绍我去了一家公司（没有面试直接进去），公司很小只有十几人左右，是团队老师的一个学生创立的公司，我就去那里实习了3个月。还好是这一次的实习，让我的简历上面有东西可写，不然自己可以断定自己是没有机会去面试其他的大公司的。</p>
