<p>这是一款颜色转换的工具，非常方便你的颜色取值。之前我都是一直使用这一款，但是最近我发现QQ的截图功能也可以吸取颜色，直接ctrl+c就可以获取16进制的颜色了，简直不要太爽。</p>
