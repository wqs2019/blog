</ul>
<p>navicat是一款数据库管理的软件，可以方便查看数据库中的各种表数据等等，但是这是一个收费的软件，大家看情况需要吧。或者你可以找一些方法来使用，大家知道就好。</p>
<ul>
<li><a href="https://www.webpagetest.org/" target="_blank" rel="noopener noreferrer">WEBPAGETEST<ExternalLinkIcon/></a></li>
