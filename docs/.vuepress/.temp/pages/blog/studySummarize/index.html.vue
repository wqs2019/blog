<h2 id="_01-前言" tabindex="-1"><a class="header-anchor" href="#_01-前言" aria-hidden="true">#</a> 01 前言</h2>
<p>很多人都有终身学习的概念，我也一直都是认同的，毕竟只有学习才会让自己适应社会，适应工作与生活。今天我就来谈一下<code v-pre>学习</code>这个比较虚、也比较广的范围，也是对自己坚持两个多月以来的感悟吧。</p>
<p>因为今年疫情的原因，自己也和大多数人一样在家里，响应政府的号召，不出门。但是想着不出门，也不能和好友相聚等，那剩下的只有什么呢？没有，剩下的只是学习。不管你是从事什么行业，学习是一件永远不过时的活动，也是一件让你觉得自己没有浪费时间的事情（有没有收获先不说）。</p>
<p>所以今年春节到现在，基本上每天都在学习和总结，从这两个多月的积累和实践下，从几个方面谈一下自己理解与感悟吧：</p>
<ul>
<li>我是否要早起和学习计划？</li>
<li>我效率不高应该投入更多时间？</li>
<li>我应该有充足的娱乐时间吗？</li>
